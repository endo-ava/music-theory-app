/**
 * 円形レイアウトの共通定数
 *
 * クロマチックサークルと五度圏の両方で使用される基本的な円形レイアウト定数を定義。
 * 音楽理論の基本である12音階に基づき、どちらのビジュアライゼーションも12セグメントで構成される。
 */

/**
 * 円形レイアウトのセグメント数
 *
 * 音楽理論における1オクターブの半音の数（12音階）に対応。
 * クロマチックサークル（C, C#, D, D#, ...）と五度圏（C, G, D, A, ...）の両方で共通。
 */
export const SEGMENT_COUNT = 12;

/**
 * 角度の基本オフセット（度）
 *
 * Cが真上（12時の位置）に来るように調整するためのオフセット。
 * - 真上の位置: -90度
 * - 最初のセグメント中心への調整: -15度（360度 / 12セグメント / 2）
 * - 合計: -105度
 *
 * この値により、position=0のセグメント（C）が円の上部に配置される。
 */
export const ANGLE_OFFSET = -105;

/**
 * 1セグメントあたりの角度（度）
 *
 * 360度をセグメント数で割った値（30度）。
 * セグメントの角度計算に使用。
 */
export const ANGLE_PER_SEGMENT = 360 / SEGMENT_COUNT;

/**
 * 1セグメントあたりの半角（ラジアン）
 *
 * セグメントの中心位置を計算する際に使用。
 * Math.PI / SEGMENT_COUNT = π/12ラジアン（15度）
 */
export const HALF_ANGLE_PER_SEGMENT_RAD = Math.PI / SEGMENT_COUNT;
